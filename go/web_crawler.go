package main

import (
	"fmt"
	"sync"
)

type UrlCache struct{
	mu 	sync.Mutex
	// map of url to body
	urls	map[string]string
}

func (c *UrlCache) SafeCache(url string, body string) {
	c.mu.Lock()
	// Add to url cache
	c.urls[url] = body
	c.mu.Unlock()
}

type Fetcher interface {
	// Fetch returns the body of URL and
	// a slice of URLs found on that page.
	Fetch(url string) (body string, urls []string, err error)
}

// Crawl uses fetcher to recursively crawl
// pages starting with url, to a maximum of depth.
func (c *UrlCache) Crawl(url string, depth int, fetcher Fetcher) {
	// TODO: Fetch URLs in parallel.
	// TODO: Don't fetch the same URL twice.
	// This implementation doesn't do either:
	if depth <= 0 {
		return
	}
	body, urls, err := fetcher.Fetch(url)
	if err != nil {
		fmt.Println(err)
		return
	}
	fmt.Printf("found: %s %q\n", url, body)
	for _, u := range urls {
		if _, ok := c.urls[u]; !ok {
			go c.Crawl(u, depth-1, fetcher)
		}
	}
	return
}

func main() {
	c := UrlCache{urls: make(map[string]string)}
	c.Crawl("https://golang.org/", 4, fetcher)
}

